/** Recursively widens all string literal leaves to `string`, preserving function signatures. */
type Widen<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends (...args: infer A) => string
    ? (...args: A) => string
    : T[K] extends object
    ? Widen<T[K]>
    : T[K]
}

export type Translations = Widen<typeof en>
